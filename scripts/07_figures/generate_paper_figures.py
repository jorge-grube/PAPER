# FIGURE 5 — Drawdown: Baseline vs FI-Expanded Static CVaR
# ─────────────────────────────────────────────────────────────────────────────
def fig5():
    base_ret = pd.read_parquet(f'{PAPER}/data/processed/panel_b_returns.parquet')['static_cvar'].dropna()
    fi_ret   = pd.read_parquet(f'{PAPER}/data/processed/panel_b_returns_fi_expanded.parquet')['static_cvar'].dropna()

    def drawdown_series(returns):
        wealth = (1 + returns).cumprod()
        peak = wealth.cummax()
        return (wealth - peak) / peak * 100  # in percent

    dd_base = drawdown_series(base_ret)
    dd_fi   = drawdown_series(fi_ret)

    # Align
    idx = dd_base.index.intersection(dd_fi.index)
    dd_base = dd_base.loc[idx]
    dd_fi   = dd_fi.loc[idx]

    fig, ax = plt.subplots(figsize=(8.5, 4.0))

    ax.fill_between(dd_base.index, dd_base, 0, alpha=0.18, color='#0072B2', zorder=1)
    ax.fill_between(dd_fi.index,   dd_fi,   0, alpha=0.18, color='#E69F00', zorder=1)
    ax.plot(dd_base.index, dd_base, color='#0072B2', lw=1.8, label='Static CVaR (Baseline)', zorder=3)
    ax.plot(dd_fi.index,   dd_fi,   color='#E69F00', lw=1.8, ls='--', label='Static CVaR (FI-Expanded)', zorder=3)

    # 2022 rate-shock band
