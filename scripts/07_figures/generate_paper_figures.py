# FIGURE 2 — Cumulative Wealth, Panel B
# ─────────────────────────────────────────────────────────────────────────────
def fig2():
    df = pd.read_parquet(f'{PAPER}/data/processed/panel_b_returns.parquet')
    df = df.dropna(how='all')

    # Compute cumulative wealth from first common date
    cols = ['equal_weight_risky','stoxx600','static_cvar','markowitz','regime_cvar_A','weighted_cvar']
    first_full = df[cols].dropna().index[0]
    sub = df.loc[first_full:, cols].fillna(0)
    wealth = (1 + sub).cumprod()

    fig, ax = plt.subplots(figsize=(8.5, 4.2))

    for col in cols:
        lw = 2.0 if col == 'static_cvar' else 1.4
        ax.plot(wealth.index, wealth[col],
                color=COLORS[col], lw=lw,
                linestyle=LINESTYLES[col],
                label=LABELS[col], zorder=3)

    # Crisis bands
    crisis_bands = [
        ('2011-07-01', '2012-09-30', 'Eurozone\nCrisis'),
        ('2020-01-01', '2020-06-30', 'COVID-19'),
        ('2022-01-01', '2022-12-31', '2022 Rate\nShock'),
    ]
    for start, end, label in crisis_bands:
        ax.axvspan(pd.Timestamp(start), pd.Timestamp(end),
                   color='#AAAAAA', alpha=0.18, zorder=1)
        mid = pd.Timestamp(start) + (pd.Timestamp(end) - pd.Timestamp(start)) / 2
        ax.text(mid, ax.get_ylim()[1] if ax.get_ylim()[1] > 1.5 else 1.7,
                label, ha='center', va='top', fontsize=8, color='#555555', style='italic')

    ax.axhline(1, color='black', lw=0.7, ls='-', zorder=2)
    ax.set_xlabel('Date')
    ax.set_ylabel('Cumulative Wealth ($1 initial)')
    # No embedded figure title - Word caption controls numbering
    ax.legend(loc='upper left', ncol=2, frameon=True, framealpha=0.92,
              edgecolor='#CCCCCC', borderpad=0.5)
    ax.grid(True, axis='y')
    ax.xaxis.set_major_formatter(matplotlib.dates.DateFormatter('%Y'))
    plt.tight_layout()
    out = f'{OUT}/figure_2_cumulative_wealth_panel_b.png'
    plt.savefig(out)
    plt.close()
    print(f'Saved: {out}')

