# APPENDIX F — Descriptive Statistics
# ─────────────────────────────────────────────────────────────────────────────
def descriptive_stats():
    from scipy import stats as sp_stats
    ret = pd.read_parquet(f'{PAPER}/data/processed/investable_returns_weekly.parquet')
    # Use risky assets only (exclude EURIBOR)
    risky_cols = ['Bloomberg_Commodity','Brent','Gold','CAC_40','DAX',
                  'EuroStoxx50','FTSE_MIB','IBEX35','StoxxEurope600','FTSE_EPRA_NAREIT_Europe']
    ret = ret[risky_cols].dropna(how='all')

    short_names = {
        'Bloomberg_Commodity':       'Bloomberg Comm.',
        'Brent':                     'Brent',
        'Gold':                      'Gold',
        'CAC_40':                    'CAC 40',
        'DAX':                       'DAX',
        'EuroStoxx50':               'EuroStoxx 50',
        'FTSE_MIB':                  'FTSE MIB',
        'IBEX35':                    'IBEX 35',
        'StoxxEurope600':            'STOXX 600',
        'FTSE_EPRA_NAREIT_Europe':   'EPRA Europe',
    }

    rows = []
    for col in risky_cols:
        s = ret[col].dropna()
        mean_ann  = s.mean() * 52 * 100
        vol_ann   = s.std() * np.sqrt(52) * 100
        skew      = sp_stats.skew(s)
        kurt      = sp_stats.kurtosis(s, fisher=False)  # excess=False → normal=3
        wealth    = (1 + s).cumprod()
        peak      = wealth.cummax()
        dd        = (wealth - peak) / peak
        max_dd    = dd.min() * 100
        start     = s.index.min().strftime('%Y-%m')
        end       = s.index.max().strftime('%Y-%m')
        rows.append({
            'Asset': short_names[col],
            'Mean%': f'{mean_ann:.2f}',
            'Vol%':  f'{vol_ann:.2f}',
            'Skew':  f'{skew:.2f}',
            'Kurt':  f'{kurt:.2f}',
            'MaxDD%':f'{max_dd:.1f}',
            'Start': start,
            'End':   end,
        })

    df_stat = pd.DataFrame(rows)
    # Save as CSV for reference
    csv_out = f'{PAPER}/paper/tables/appendix_f_descriptive_stats.csv'
    df_stat.to_csv(csv_out, index=False)
    print(f'Saved descriptive stats: {csv_out}')

    # Pairwise correlations (weekly returns, full sample, risky assets)
    corr = ret[risky_cols].corr()
    short_list = [short_names[c] for c in risky_cols]
    corr.index = short_list
    corr.columns = short_list
    corr_out = f'{PAPER}/paper/tables/appendix_f_correlations.csv'
    corr.round(2).to_csv(corr_out)
    print(f'Saved correlations: {corr_out}')

    return df_stat, corr

